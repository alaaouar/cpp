#include "RPN.hpp"

int main(int argc, char **argv)
{
    if (argc != 2)
    {
        std::cout << "Error: invalid number of arguments." << std::endl;
        return (1);
    }
    RPN rpn;
    try
    {
        std::cout << rpn.evaluate(argv[1]) << std::endl;
    }
    catch(const std::exception& e)
    {
        std::cerr << e.what() << '\n';
    }
    
    return (0);
}